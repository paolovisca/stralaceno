Formato [elenco_posts]:
<post_id>::<post_author>::<post_date>::<post_status>
 	<post_id>	: numero incrementale che identifica il post all'interno del topic
	<post_caption>	: titolo del post
	<post_status>	: stato del post:
			  visible	: il post e' visibile
			  censored	: il topic e' censurato
			  hidden	: il topic e' invisibile
Formato [post_text_X]:
E' il testo del post, puo' essere su piu' righe, nessuna delle quali vuote

