Per scrivere un nuovo messaggio, basta premere sul tasto "Rispondi", ed immettere il proprio nome ed il testo
&nbsp;
Se si vuole rispondere ad uno specifico messaggio, citandone una parte, si deve cliccare sul tasto "Citazione": oltre al testo del nuovo messaggio, si potra' selezionare la parte del vecchio messaggio che si vuole citare.
&nbsp;
Per aprire una nuova discussione, invece, bastera' premere il tasto "Nuova Disc.", che richiede, oltre al testo del messaggio, anche il titolo della discussione.